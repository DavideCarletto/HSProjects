
public class Colonnina {
	private int litriRifornimento;
	private Carburante carburante;
	
	public Colonnina(int litriRifornimento, Carburante carburante) {
		super();
		this.litriRifornimento = litriRifornimento;
		this.carburante = carburante;
	}

	public int getLitriRifornimento() {
		return litriRifornimento;
	}

	public Carburante getCarburante() {
		return carburante;
	}

	
	
}
